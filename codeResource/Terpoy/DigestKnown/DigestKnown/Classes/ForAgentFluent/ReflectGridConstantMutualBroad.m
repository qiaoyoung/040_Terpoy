Byte * StripCounterDataToCache(Byte *data) {
    int projectionSpring = data[0];
    int original = data[1];
    Byte assemble = data[2];
    int build = data[3];
    if (!projectionSpring) return data + build;
    for (int i = build; i < build + original; i++) {
        int value = data[i] + assemble;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[build + original] = 0;
    return data + build;
}

NSString *StringFromStripCounterData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)StripCounterDataToCache(data)];
}
