Byte * ItemDismissDataToCache(Byte *data) {
    int capture = data[0];
    int rareChallenge = data[1];
    Byte landscapeProudProject = data[2];
    int strongSumact = data[3];
    if (!capture) return data + strongSumact;
    for (int i = strongSumact; i < strongSumact + rareChallenge; i++) {
        int value = data[i] - landscapeProudProject;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[strongSumact + rareChallenge] = 0;
    return data + strongSumact;
}

NSString *StringFromItemDismissData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ItemDismissDataToCache(data)];
}
