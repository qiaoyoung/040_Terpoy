Byte * SoftCap_DataToCache(Byte *data) {
    int entrySearch = data[0];
    int quality = data[1];
    int loosePin = data[2];
    if (!entrySearch) return data + loosePin;
    for (int i = 0; i < quality / 2; i++) {
        int begin = loosePin + i;
        int end = loosePin + quality - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[loosePin + quality] = 0;
    return data + loosePin;
}

NSString *StringFromSoftCap_Data(Byte *data) {
    return [NSString stringWithUTF8String:(char *)SoftCap_DataToCache(data)];
}  
