Byte * StackDataToCache(Byte *data) {
    int chapterDevice = data[0];
    int weaveSend = data[1];
    Byte deliverInfrastructure = data[2];
    int episode = data[3];
    if (!chapterDevice) return data + episode;
    for (int i = episode; i < episode + weaveSend; i++) {
        int value = data[i] + deliverInfrastructure;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[episode + weaveSend] = 0;
    return data + episode;
}

NSString *StringFromStackData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)StackDataToCache(data)];
